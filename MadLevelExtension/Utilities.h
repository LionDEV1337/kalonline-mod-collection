void readConfig()
{
	string prefix = "[Level ";
	ifstream f; 
	string s;
	string t;
	int level  = 0;
	__int64 exp = 0;
	ifstream ifile("exptable.txt");
	if (ifile) 
	{
		f.open("exptable.txt", ios::in); 
		while (!f.eof())        
		{
			getline(f, s);       
			if (s.substr(0, prefix.size()) == prefix) 
			{
				t = (s.substr(prefix.size(),10));	
				level =  atoi( t.c_str() );
				t = (s.substr(13,s.size()));	
				exp =   _atoi64( t.c_str());
			}		
			ExpTable[level] = exp;

		}
		f.close();            
	}
}

